exam_mark = int(input("mark: "))
if 0 <= exam_mark <= 40:
    print("Grade is U")
if 41 <= exam_mark <= 50:
    print("Grade is E")
if 51 <= exam_mark <= 60:
    print("Grade is D")
if 61 <= exam_mark <= 70:
    print("Grade is C")
if 71 <= exam_mark <= 80:
    print("Grade is B")
if 81 <= exam_mark <= 90:
    print("Grade is A")
